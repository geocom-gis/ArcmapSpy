            AddEsriArcmapUiInterfaces();
            AddEsriLocationInterfaces();
            Children.Sort((a, b) => string.Compare(a.Title, b.Title, StringComparison.InvariantCultureIgnoreCase));
        }

        private void AddAsInterface<T>() where T : class
        {
            T asObj = _comObj as T;

            if (asObj != null)
            {
                Type typeOfComObj = typeof(T);
                string title = string.Format("as {0}", typeOfComObj.Name);
                ComObjectViewModel child = new ComObjectViewModel(this, asObj, typeOfComObj, title);
                Children.Add(child);
            }
        }

